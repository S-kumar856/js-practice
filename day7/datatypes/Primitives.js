//  Primitive datatypes  are 6 types 
// String, number, boolean,undefined, null, symbol

// 1) Number
let mobilePrice = 19999;
console.log('Type of mobilePrice:', typeof(mobilePrice))
// output: "Type of mobilePrice: number"

// 2) String
let mobileName = "Apple";
console.log("Type of mobileName:", typeof(mobileName))
// output : "Type of mobileName: string"

// 3) Boolean

let isMobileAvailable = true;
console.log("Type of isMobileAvailable:", typeof(isMobileAvailable))
// ooutput : "Type of isMobileAvailable: boolean"

// 4) Undefined
let mobileColor;
console.log('Type of mobileColor:', typeof(mobileColor))
// output : "Type of mobileColor: undefined"

// 5) Null
let offer = null;
console.log("Type of offer:", typeof(offer))
console.log("Value of offer:", offer)
// output: "Type of offer: object"
// output : "Type of offer: null"
